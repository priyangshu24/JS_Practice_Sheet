//Loop
//print the number from 1 to 10 using for loop
console.log("Using a for loop");
for (let i = 1; i <= 10; i++) {
  console.log(i);
}

//print the number from 10 to 1 using while loop
console.log("Using a while loop");
let j = 10;
while (j >= 1) {
  console.log(j);
  j--;
}

//print the number from 1 to 10 using do while loop
console.log("Using a do while loop");
let k = 1;
do {
  console.log(k);
  k++;
} while (k <= 10);
